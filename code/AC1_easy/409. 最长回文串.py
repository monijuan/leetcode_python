from leetcode_python.utils import *


class Solution:
    def __init__(self):
        pass

    def longestPalindrome(self, s: str) -> int:
        res = 0
        max_odd = None
        for count in collections.Counter(s).values():
            if count&1: # 奇数次数
                if not max_odd: # 第一个奇数
                    max_odd = count
                elif count>max_odd: # 有更大奇数
                    res += max_odd-1
                    max_odd = count
                else:# 较小奇数
                    res += count-1
            else:  # 偶数次数
                res+=count

        if max_odd:res+=max_odd # 最长奇数可以留

        return res


def test(data_test):
    s = Solution()
    data = data_test  # normal
    # data = [list2node(data_test[0])]  # list转node
    return s.longestPalindrome(*data)


def test_obj(data_test):
    result = [None]
    obj = Solution(*data_test[1][0])
    for fun, data in zip(data_test[0][1::], data_test[1][1::]):
        if data:
            res = obj.__getattribute__(fun)(*data)
        else:
            res = obj.__getattribute__(fun)()
        result.append(res)
    return result


