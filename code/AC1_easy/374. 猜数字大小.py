class Solution:
    def guessNumber(self, n: int) -> int:
        l, r = 1, n
        while l < r:
            m = l + (r-l) // 2
            res = guess(m)
            # print(m,res)
            if res == 1:
                l = m + 1
            elif res == -1:
                r = m
            else:
                return m
        return l


