from leetcode_python.utils import *


class Solution:
    def maximumBobPoints_wtl(self, numArrows: int, aliceArrows: List[int]) -> List[int]:
        resli,maxsum = [],0
        def dfs(sumb,idx,remain,res):
            # print(idx,sumb,res)
            nonlocal numArrows,aliceArrows,resli,maxsum
            if idx==0:
                res_ = res.copy()
                res_[idx]=remain
                if remain > aliceArrows[idx]:
                    sumb +=idx
                if sumb>maxsum:
                    resli = res_
                    maxsum=sumb
            else:
                if remain>aliceArrows[idx]:
                    res_ = res.copy()
                    res_[idx]=aliceArrows[idx]+1
                    dfs(sumb+idx,idx-1,remain-aliceArrows[idx]-1,res_)
                dfs(sumb,idx-1,remain,res)
        dfs(0,11,numArrows,[0]*12)
        return resli

def test(data_test):
    s = Solution()
    data = data_test  # normal
    # data = [List2Node(data_test[0])]  # list转node
    return s.maximumBobPoints(*data)


def test_obj(data_test):
    result = [None]
    obj = Solution(*data_test[1][0])
    for fun, data in zip(data_test[0][1::], data_test[1][1::]):
        if data:
            res = obj.__getattribute__(fun)(*data)
        else:
            res = obj.__getattribute__(fun)()
        result.append(res)
    return result


