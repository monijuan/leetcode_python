from leetcode_python.utils import *

class Node:
    def __init__(self, val: int = 0, left: 'Node' = None, right: 'Node' = None, next: 'Node' = None):
        self.val = val
        self.left = left
        self.right = right
        self.next = next

class Solution:
    def __init__(self):
        pass

    def connect(self, root: 'Node') -> 'Node':
        this_line = [root]
        while this_line:
            next_line = []
            length = len(this_line)
            for index,node in enumerate(this_line):
                if index<length-1: node.next = this_line[index+1]
                if node and node.left:next_line.append(node.left)
                if node and node.right:next_line.append(node.right)
            this_line = next_line
        return root

def test(data_test):
    s = Solution()
    data = data_test  # normal
    # data = [list2node(data_test[0])]  # list转node
    return s.connect(*data)


def test_obj(data_test):
    result = [None]
    obj = Solution(*data_test[1][0])
    for fun, data in zip(data_test[0][1::], data_test[1][1::]):
        if data:
            res = obj.__getattribute__(fun)(*data)
        else:
            res = obj.__getattribute__(fun)()
        result.append(res)
    return result


