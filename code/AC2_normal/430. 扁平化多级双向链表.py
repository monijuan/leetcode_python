from leetcode_python.utils import *


class Solution:
    def __init__(self):
        pass

    def flatten(self, head: 'Node') -> 'Node':
        if not head:
            return head
        elif head.child:
            next_node = self.flatten(head.child)        # find mid
            if head.next:
                mid_last = next_node
                while mid_last.next:mid_last=mid_last.next
                head.next.prev = mid_last               # right -> mid
                mid_last.next = head.next               # mid   -> right
            next_node.prev = head                       # mid   -> left
            head.next = next_node                       # left  -> mid
            head.child = None
        else:
            head.next = self.flatten(head.next)         # left->mid / mid->left
        return head


def test(data_test):
    s = Solution()
    return s.flatten(*data_test)


def test_obj(data_test):
    result = [None]
    obj = Solution(*data_test[1][0])
    for fun, data in zip(data_test[0][1::], data_test[1][1::]):
        if data:
            res = obj.__getattribute__(fun)(*data)
        else:
            res = obj.__getattribute__(fun)()
        result.append(res)
    return result


