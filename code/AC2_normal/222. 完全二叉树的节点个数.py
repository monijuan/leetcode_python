from leetcode_python.utils import *

def Tree2List(root):
    """二叉树 -> 列表"""
    if not root:return []
    res = []
    last = [root]
    while last:
        now = []
        for node in last:
            if node:
                res.append(node.val)
                now.append(node.left)
                now.append(node.right)
            else:
                res.append(None)
        last = now
    while len(res)>1 and res[-1]==None: res.pop(-1)
    return res

class Solution:
    def countNodes(self, root: TreeNode) -> int:
        return len(Tree2List(root))


def test(data_test):
    s = Solution()
