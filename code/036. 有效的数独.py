from leetcode_python.utils import *


class Solution:
    def __init__(self):
        pass

    def __isRep(self,line:List[str])->bool:
        line_str = ''.join(line).replace('.','')
        return len(set(line_str))!=len(line_str)

    def isValidSudoku(self, board: List[List[str]]) -> bool:
        for line in board:
            if self.__isRep(line):
                return False
        for line in list(map(list, zip(*board))):
            if self.__isRep(line):
                return False
        



