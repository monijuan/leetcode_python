import time
from typing import List

class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right

class Solution:
    def __init__(self):
        self.res = []
        pass

    def first(self, root:TreeNode, route:List[int], sum:int):
        if root is None: return
        sum+=root.val
        route.append(root.val)
        if sum==self.target and root.left is None and root.right is None:
            self.res.append(route[:])
        self.first(root.left,route,sum)
        self.first(root.right,route,sum)
        route.pop()

    def pathSum(self, root: TreeNode, target: int) -> List[List[int]]:
        self.target = target
        self.first(root,[],0)
        return self.res


def test(data_test):
    s = Solution()
    return s.getResult(*data_test)


def test_obj(data_test):
    result = [None]
    obj = Solution(*data_test[1][0])
    for fun, data in zip(data_test[0][1::], data_test[1][1::]):
        if data:
            res = obj.__getattribute__(fun)(*data)
        else:
            res = obj.__getattribute__(fun)()
        result.append(res)
    return result


