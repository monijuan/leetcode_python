import time
from typing import List
# 本题与主站 154 题相同

class Solution:
    def __init__(self):
        pass

    def minArray(self, numbers: List[int]) -> int:
        return min(numbers)


def test(data_test):
    s = Solution()
    return s.minArray(*data_test)


